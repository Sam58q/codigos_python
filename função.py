#Função com retorno
def somar(num1,num2):
    resultado = num1 + num2
    return resultado#Devolvendo o valor para quem chamou a função

#Chamando a função 
print(somar(10,5))
print('-'*50)

#Função sem retorno
def multiplicar(valor1,valor2):
    resultado = valor1 * valor2
    print(resultado)

#Chamando a função
multiplicar(2,6)
print('-'*50)

